package me.chinatsui.exercise.hihocoder;

import java.util.Scanner;

/**
 * 计算 A + B
 */
public class Main1000 {

    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);

        while (in.hasNext()) {
            int a = in.nextInt();
            int b = in.nextInt();
            System.out.println(a + b);
        }

    }

}

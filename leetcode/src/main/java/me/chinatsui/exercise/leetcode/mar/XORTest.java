public class XORTest {

    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        System.out.println(b ^ b);
        System.out.println(a ^ b);
        System.out.println(a ^ b ^ b);

        System.out.println(0 ^ 18);
    }

}
